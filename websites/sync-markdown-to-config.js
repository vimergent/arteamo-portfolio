/**
 * Sync Script: Markdown → project-config.js
 *
 * Reads all markdown files from content/projects/ and generates
 * project-config.js for the website to use.
 *
 * This enables full CMS integration where:
 * 1. CMS edits markdown files
 * 2. This script runs on deploy (or manually)
 * 3. Website reads generated project-config.js
 */

const fs = require('fs');
const path = require('path');
const matter = require('gray-matter');

// Directories
const projectsDir = path.join(__dirname, 'content/projects');
const outputPath = path.join(__dirname, 'project-config.js');

/**
 * Convert markdown frontmatter to project config format
 * @param {Object} data - Frontmatter data
 * @param {string} filename - Markdown filename
 * @returns {Object} - Project config object
 */
function convertToProjectConfig(data, filename) {
  // Extract year and slug from filename (e.g., "2024-apartament-flavia-garden-2024.md")
  const nameMatch = filename.match(/^\d{4}-(.+)\.md$/);
  const projectSlug = nameMatch ? nameMatch[1] : filename.replace('.md', '');

  return {
    // Multi-language names
    name: {
      bg: data.name_bg || '',
      en: data.name_en || '',
      ru: data.name_ru || '',
      es: data.name_es || '',
      he: data.name_he || '',
      zh: data.name_zh || ''
    },

    // Multi-language subtitles
    subtitle: {
      bg: data.subtitle_bg || '',
      en: data.subtitle_en || '',
      ru: data.subtitle_ru || '',
      es: data.subtitle_es || '',
      he: data.subtitle_he || '',
      zh: data.subtitle_zh || ''
    },

    // Multi-language descriptions
    description: {
      bg: data.description_bg || '',
      en: data.description_en || '',
      ru: data.description_ru || '',
      es: data.description_es || '',
      he: data.description_he || '',
      zh: data.description_zh || ''
    },

    // Project details
    category: data.category || 'residential',
    year: data.year || new Date().getFullYear(),
    area: data.area || 0,
    location: data.location || '',

    // Images (Cloudinary URLs)
    coverImage: data.cover_image || '',
    images: data.images || [],

    // Publishing settings
    published: data.published !== false, // Default to true
    featured: data.featured || false,
    order: data.order || 999,

    // Metadata
    slug: projectSlug,
    originalKey: data.original_key || ''
  };
}

/**
 * Read and parse all markdown files
 * @returns {Array} - Array of project objects
 */
function parseMarkdownFiles() {
  const projects = [];

  // Get all markdown files
  const files = fs.readdirSync(projectsDir)
    .filter(f => f.endsWith('.md'))
    .sort(); // Sort alphabetically

  console.log(`\n📂 Found ${files.length} markdown files\n`);

  files.forEach(filename => {
    const filePath = path.join(projectsDir, filename);

    try {
      // Read and parse markdown
      const content = fs.readFileSync(filePath, 'utf8');
      const doc = matter(content);

      // Skip unpublished projects
      if (doc.data.published === false) {
        console.log(`   ⏭️  Skipped (unpublished): ${filename}`);
        return;
      }

      // Convert to project config format
      const project = convertToProjectConfig(doc.data, filename);
      projects.push(project);

      console.log(`   ✅ Parsed: ${filename} → ${project.name.en || project.name.bg}`);

    } catch (error) {
      console.error(`   ❌ Error parsing ${filename}:`, error.message);
    }
  });

  return projects;
}

/**
 * Sort projects by year (descending) and name
 * @param {Array} projects - Array of project objects
 * @returns {Array} - Sorted array
 */
function sortProjects(projects) {
  return projects.sort((a, b) => {
    // First by year (descending)
    if (b.year !== a.year) {
      return b.year - a.year;
    }

    // Then by order field
    if (a.order !== b.order) {
      return a.order - b.order;
    }

    // Then by English name
    const nameA = a.name.en || a.name.bg || '';
    const nameB = b.name.en || b.name.bg || '';
    return nameA.localeCompare(nameB);
  });
}

/**
 * Generate project-config.js file
 * @param {Array} projects - Array of project objects
 */
function generateConfigFile(projects) {
  // Create JavaScript object
  const configObject = {};

  projects.forEach(project => {
    // Use original_key as the key if available, otherwise use slug
    const key = project.originalKey || project.slug;
    configObject[key] = project;
  });

  // Generate JavaScript file content
  const fileContent = `/**
 * Project Configuration
 *
 * AUTO-GENERATED FILE - DO NOT EDIT MANUALLY
 *
 * This file is automatically generated from markdown files in content/projects/
 * by the sync-markdown-to-config.js script.
 *
 * To modify projects:
 * 1. Edit markdown files in content/projects/
 * 2. Run: node sync-markdown-to-config.js
 * 3. Or use the CMS at /admin-cms/ (commits trigger auto-sync)
 *
 * Last updated: ${new Date().toISOString()}
 * Projects count: ${projects.length}
 */

const projectConfig = ${JSON.stringify(configObject, null, 2)};

// Export for use in website
if (typeof module !== 'undefined' && module.exports) {
  module.exports = projectConfig;
}
`;

  // Write to file
  fs.writeFileSync(outputPath, fileContent, 'utf8');
  console.log(`\n💾 Generated: project-config.js`);
  console.log(`   📊 ${projects.length} projects included\n`);
}

/**
 * Main execution
 */
function main() {
  console.log('\n🔄 MARKDOWN → PROJECT-CONFIG.JS SYNC\n');
  console.log('━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━\n');
  console.log(`📍 Reading from: ${projectsDir}`);
  console.log(`📍 Writing to: ${outputPath}\n`);
  console.log('━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━\n');

  try {
    // Parse all markdown files
    let projects = parseMarkdownFiles();

    // Sort projects
    projects = sortProjects(projects);

    console.log('\n━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━\n');

    // Generate config file
    generateConfigFile(projects);

    console.log('━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━\n');
    console.log('✅ SYNC COMPLETE!\n');
    console.log('Next steps:');
    console.log('   1. Review project-config.js');
    console.log('   2. Test website locally');
    console.log('   3. Commit and deploy to Netlify\n');

  } catch (error) {
    console.error('\n❌ SYNC FAILED:', error.message);
    console.error(error.stack);
    process.exit(1);
  }
}

// Run the script
main();
