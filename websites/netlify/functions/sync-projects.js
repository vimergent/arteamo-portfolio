/**
 * Netlify Function: Sync Projects from CMS to project-config.js
 *
 * This function runs automatically when content is published in Netlify CMS.
 * It reads all project markdown files from /content/projects/ and generates
 * the project-config.js file that the website uses.
 *
 * Trigger: Netlify CMS publish event
 * Method: POST
 * Path: /.netlify/functions/sync-projects
 */

const fs = require('fs').promises;
const path = require('path');
const matter = require('gray-matter');

exports.handler = async (event, context) => {
  try {
    // Only process POST requests from CMS
    if (event.httpMethod !== 'POST') {
      return {
        statusCode: 405,
        body: JSON.stringify({ error: 'Method Not Allowed' })
      };
    }

    console.log('Starting project sync...');

    // Path to content directory
    const contentDir = path.join(process.cwd(), 'content', 'projects');
    const configPath = path.join(process.cwd(), 'project-config.js');

    // Read all markdown files from content/projects/
    const files = await fs.readdir(contentDir);
    const projectFiles = files.filter(f => f.endsWith('.md'));

    console.log(`Found ${projectFiles.length} project files`);

    // Parse each markdown file and extract frontmatter
    const projects = {};

    for (const file of projectFiles) {
      const filePath = path.join(contentDir, file);
      const fileContent = await fs.readFile(filePath, 'utf8');
      const { data } = matter(fileContent);

      // Create project object from frontmatter
      const projectKey = data.name_en || data.name_bg;

      projects[projectKey] = {
        name: {
          bg: data.name_bg || '',
          en: data.name_en || '',
          ru: data.name_ru || '',
          es: data.name_es || '',
          he: data.name_he || '',
          zh: data.name_zh || ''
        },
        subtitle: {
          bg: data.subtitle_bg || '',
          en: data.subtitle_en || '',
          ru: data.subtitle_ru || '',
          es: data.subtitle_es || '',
          he: data.subtitle_he || '',
          zh: data.subtitle_zh || ''
        },
        description: {
          bg: data.description_bg || '',
          en: data.description_en || '',
          ru: data.description_ru || '',
          es: data.description_es || '',
          he: data.description_he || '',
          zh: data.description_zh || ''
        },
        category: data.category || 'residential',
        year: parseInt(data.year) || new Date().getFullYear(),
        area: parseInt(data.area) || 0,
        location: data.location || '',
        coverImage: data.cover_image || '',
        images: data.images || [],
        published: data.published !== false,
        featured: data.featured === true,
        order: parseInt(data.order) || 999
      };
    }

    // Sort projects by order, then by year (descending)
    const sortedProjects = Object.entries(projects)
      .sort(([, a], [, b]) => {
        if (a.order !== b.order) return a.order - b.order;
        return b.year - a.year;
      })
      .reduce((obj, [key, value]) => {
        obj[key] = value;
        return obj;
      }, {});

    // Generate JavaScript file content
    const jsContent = `/**
 * Studio Arteamo - Project Configuration
 * Auto-generated by Netlify CMS sync function
 * Last updated: ${new Date().toISOString()}
 *
 * This file is automatically generated from content/projects/*.md files.
 * DO NOT EDIT MANUALLY - changes will be overwritten.
 *
 * To update projects, use the CMS at /admin-cms/
 */

const projectConfig = ${JSON.stringify(sortedProjects, null, 2)};

// Export for use in website
if (typeof module !== 'undefined' && module.exports) {
  module.exports = projectConfig;
}
`;

    // Write the generated config file
    await fs.writeFile(configPath, jsContent, 'utf8');

    console.log(`Successfully synced ${Object.keys(projects).length} projects`);

    return {
      statusCode: 200,
      headers: {
        'Content-Type': 'application/json'
      },
      body: JSON.stringify({
        success: true,
        message: `Synced ${Object.keys(projects).length} projects`,
        projects: Object.keys(projects)
      })
    };

  } catch (error) {
    console.error('Sync error:', error);

    return {
      statusCode: 500,
      headers: {
        'Content-Type': 'application/json'
      },
      body: JSON.stringify({
        success: false,
        error: error.message,
        stack: process.env.NODE_ENV === 'development' ? error.stack : undefined
      })
    };
  }
};
